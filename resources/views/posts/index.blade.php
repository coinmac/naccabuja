        <h3> and Objectives</h3>
        <ul>
            <li>
        To promote the development of trade, commerce, investment and industrial
        technological relationships between the public and private sectors of the Federal
        Republic of Nigeria (Nigeria) and the United States of America (United States).</li>
        <li>
        To promote and develop economic, cultural and social interests between Nigeria and
        the United States.</li>
        
        <li>To promote and develop contacts, foster mutual understanding and encourage
        sound and ethical business relationships between Nigerians and Americans in
        business.</li>
        <li>To foster relationships with other Chambers of Commerce in Nigeria and the United
        States.</li>
        <li>To encourage the establishment of Chapters of the Chamber throughout Nigeria.</li>
        <li>To promote the consideration of all matters relating to Nigerian-American trade,
        commerce and investment and to communicate such information to its members
        and to the Nigerian-American community.</li>
        <li>To establish and maintain a Chamber, for persons and organisations concerned with
