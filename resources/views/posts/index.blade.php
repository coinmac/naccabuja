<p>
        <div class="jumbotron lead">
        <span class="h4">The Nigerian-American Chamber of Commerce (NACC)</span> is the first and oldest bilateral chamber of
        commerce in Nigeria. It was established in 1960 to enhance business relations between the
        United States of America and Nigeria. The Chamber started as a Luncheon Club then known as
        the Nigerian-American Friendship Club with few eminent Nigerians and an American namely,
        Late Sir. Mobolaji Bank Anthony, Chief (DR.) Christopher Ogunbanjo and Bob Fleming, the Public
        Relations Officer of Mobil. The Luncheon Club gradually metamorphosed into NACC with Late Sir
        Mobolaji Bank Anthony as its founding President.</div>
</p>
<p>
        More than 56 years on, the NACC has grown into a dynamic organization. It stands as a pillar of
        the relationship between the United States of America and Nigeria, serving as an important
        catalyst in bringing together people and ideals to bolster bilateral commercial relations between
        Nigeria and the United States.
</p>   
<p> 
        The NACC has close to 1000 members spanning different sectors of the economy in 8 chapters
        across the country and in Houston, USA. This has enabled the Chamber to realize its lofty aims
        and objectives which include the fostering of trade and commerce investment between Nigeria
        and the USA and liaising with the Nigerian Government and its agencies throughout the country
        on trade and industry related matters.
</p>
<p>        
        We create value for our members continuously and facilitate business-to-business relationships.
        The services we offer to our members include the organization of trade missions into the two
        countries, sponsorship of trainings, conferences and seminars addressed by leading authorities
        in different fields, affording members and non-members a platform for the discussion of topical
        issues affecting the economy in general. We also partner with organizations and in the execution
        of their corporate objectives.        
</p>

<p>
